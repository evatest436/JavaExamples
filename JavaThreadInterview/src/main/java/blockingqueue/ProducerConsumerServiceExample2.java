package blockingqueue;
import java.util.concurrent.BlockingQueue;
import java.util.concurrent.LinkedBlockingQueue;
import java.util.concurrent.ThreadLocalRandom;

public class ProducerConsumerServiceExample2 {

	static int BOUND = 10;
	static int N_PRODUCERS = 4;
	static int N_CONSUMERS = Runtime.getRuntime().availableProcessors();
	static int poisonPill = Integer.MAX_VALUE;
	static int poisonPillPerProducer = N_CONSUMERS / N_PRODUCERS;
	static int mod = N_CONSUMERS % N_PRODUCERS;

	public static void main(String[] args) {
		BlockingQueue<Integer> queue = new LinkedBlockingQueue<>(BOUND);

		for (int i = 1; i < N_PRODUCERS; i++) {
			new Thread(new NumbersProducer(queue, poisonPill, poisonPillPerProducer)).start();
		}

		for (int j = 0; j < N_CONSUMERS; j++) {
			new Thread(new NumbersConsumer(queue, poisonPill)).start();
		}

		new Thread(new NumbersProducer(queue, poisonPill, poisonPillPerProducer + mod)).start();
	}

	public static class NumbersProducer implements Runnable {

		private BlockingQueue<Integer> numbersQueue;
		private final int poisonPill;
		private final int poisonPillPerProducer;

		public NumbersProducer(BlockingQueue<Integer> numbersQueue, int poisonPill, int poisonPillPerProducer) {
			this.numbersQueue = numbersQueue;
			this.poisonPill = poisonPill;
			this.poisonPillPerProducer = poisonPillPerProducer;
		}

		public void run() {
			try {
				generateNumbers();
			} catch (InterruptedException e) {
				Thread.currentThread().interrupt();
			}
		}

		private void generateNumbers() throws InterruptedException {
			for (int i = 0; i < 100; i++) {
				numbersQueue.put(ThreadLocalRandom.current().nextInt(100));
			}
			for (int j = 0; j < poisonPillPerProducer; j++) {
				numbersQueue.put(poisonPill);
			}
		}
	}

	public static class NumbersConsumer implements Runnable {
		
		private BlockingQueue<Integer> queue;
		private final int poisonPill;

		public NumbersConsumer(BlockingQueue<Integer> queue, int poisonPill) {
			this.queue = queue;
			this.poisonPill = poisonPill;
		}

		public void run() {
			try {
				while (true) {
					Integer number = queue.take();
					if (number.equals(poisonPill)) {
						return;
					}
					System.out.println(Thread.currentThread().getName() + " result: " + number);
				}
			} catch (InterruptedException e) {
				Thread.currentThread().interrupt();
			}
		}
	}
}
